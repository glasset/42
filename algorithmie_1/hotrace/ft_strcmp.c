int				ft_strcmp(char *s1, char *s2)
{
	int			u;

	u = 0;
	while (s1[u] || s2[u])
	{
		if (s1[u] != s2[u])
			return (s1[u] - s2[u]);
		u++;
	}
	return (0);
}
