#include "fdf.h"

t_point	ft_iso(int x, int y, int z)
{
	t_point		t;
	int			size;
	int			size_z;

	size = -100;
	size_z = -8;
	t.x = (707 * (y * size - x * size )) / 1000;
	t.y = ((816 * (z * size_z)) - (408 * (y * size + x * size))) / 1000;
	return (t);
}
