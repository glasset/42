#include <stdlib.h>

void		ft_strtabdel(char ***tab)
{
	int		i;

	i = -1;
	if (!(*tab))
		return ;
	while ((*tab)[++i])
		free((*tab)[i]);
	free(*tab);
	*tab = NULL;
}
